package kr.co.sist.exam.domain;

public class Emp {
	private int empno, sal, mgr;
	private String ename, job, hiredate;
	

	public int getEmpno() {
		return empno;
	}

	public int getSal() {
		return sal;
	}

	public int getMgr() {
		return mgr;
	}

	public String getEname() {
		return ename;
	}

	public String getJob() {
		return job;
	}

	public String getHiredate() {
		return hiredate;
	}
	
	
	
}//class
